const buildCategoryHints = (
  groups: Array<{ hint: string; items: string[] }>,
): Record<string, string[]> => (
  groups.reduce<Record<string, string[]>>((result, group) => {
    group.items.forEach(item => {
      if (!result[item]) result[item] = [group.hint];
    });
    return result;
  }, {})
);

